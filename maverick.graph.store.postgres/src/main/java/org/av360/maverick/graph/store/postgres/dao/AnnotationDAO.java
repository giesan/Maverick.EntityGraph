package org.av360.maverick.graph.store.postgres.dao;

import com.fasterxml.jackson.databind.JsonNode;
import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Data;
import lombok.NoArgsConstructor;
import org.springframework.data.annotation.Id;
import org.springframework.data.relational.core.mapping.Column;
import org.springframework.data.relational.core.mapping.Table;

@Data
@Builder
@NoArgsConstructor
@AllArgsConstructor
@Table(value = "attribute_annotations")
public class AnnotationDAO extends Base {
    @Id
    private Long id;

    @Column("annotation_name")
    private String annotationName;

    @Column("annotation_value")
    private JsonNode annotationValue;

    @Column("attribute_id")
    private String attributeId;

}